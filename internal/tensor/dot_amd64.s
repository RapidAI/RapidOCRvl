// ropeAVX: applies multi-dimensional RoPE using precomputed cos/sin tables
// func ropeAVX(x []float32, cosTable, sinTable []float32, heads, dim int)
TEXT ·ropeAVX(SB), NOSPLIT, $0-88
	MOVQ x_base+0(FP), SI
	MOVQ cosTable_base+24(FP), DI
	MOVQ sinTable_base+48(FP), R8
	MOVQ heads+72(FP), AX     // number of heads
	MOVQ dim+80(FP), BX       // dim per head
	MOVQ BX, CX               // CX = dim
	SHRQ $1, CX               // CX = half = dim/2

	// dimBytes = dim * 4
	IMULQ $4, BX              // BX = dim * 4 bytes (stride between heads)

ropeHeadLoop:
	TESTQ AX, AX
	JZ ropeDone
	DECQ AX

	// Save base pointer for this head
	MOVQ SI, R11              // R11 = &x[head * dim] (saved)
	// R10 = &x[head * dim + half] = SI + half*4
	LEAQ (SI)(CX*4), R10      // R10 = SI + half*4

	// Process half elements, 8 at a time
	MOVQ CX, R9               // R9 = remaining count

ropeInnerLoop:
	CMPQ R9, $8
	JB ropeInnerTail

	// Load 8 cos, 8 sin, 8 a (first half), 8 b (second half)
	VMOVUPS (DI), Y0          // cos
	VMOVUPS (R8), Y1          // sin
	VMOVUPS (SI), Y2          // a = x[base+i..i+7]
	VMOVUPS (R10), Y3         // b = x[base+half+i..i+7]

	// new_a = a*cos - b*sin
	VMULPS Y0, Y2, Y4
	VMULPS Y1, Y3, Y5
	VSUBPS Y5, Y4, Y4         // Y4 = new_a

	// new_b = b*cos + a*sin
	VMULPS Y0, Y3, Y6
	VMULPS Y1, Y2, Y7
	VADDPS Y7, Y6, Y6         // Y6 = new_b

	VMOVUPS Y4, (SI)          // store new_a
	VMOVUPS Y6, (R10)         // store new_b

	ADDQ $32, SI              // advance by 8 floats = 32 bytes
	ADDQ $32, R10
	ADDQ $32, DI              // advance cos
	ADDQ $32, R8              // advance sin
	SUBQ $8, R9
	JMP ropeInnerLoop

ropeInnerTail:
	CMPQ R9, $0
	JE ropeHeadAdvance

	// Scalar tail: one element at a time
	MOVSS (DI), X0            // cos
	MOVSS (R8), X1            // sin
	MOVSS (SI), X2            // a
	MOVSS (R10), X3           // b
	// new_a = a*cos - b*sin
	MULSS X0, X2              // X2 = a*cos
	MULSS X1, X3              // X3 = b*sin
	MOVSS X2, X4              // X4 = a*cos
	SUBSS X3, X4              // X4 = a*cos - b*sin = new_a
	// new_b = b*cos + a*sin
	MOVSS (R10), X5           // reload b
	MULSS X0, X5              // X5 = b*cos
	MOVSS (SI), X6            // reload a (original, before store)
	MULSS X1, X6              // X6 = a*sin
	ADDSS X6, X5              // X5 = b*cos + a*sin = new_b
	// Store
	MOVSS X4, (SI)
	MOVSS X5, (R10)
	ADDQ $4, SI
	ADDQ $4, R10
	ADDQ $4, DI
	ADDQ $4, R8
	DECQ R9
	JMP ropeInnerTail

ropeHeadAdvance:
	VZEROUPPER
	// Reset cos/sin pointers for next head (they restart from 0)
	MOVQ cosTable_base+24(FP), DI
	MOVQ sinTable_base+48(FP), R8
	// Advance x pointer by dim*4 bytes to next head
	MOVQ R11, SI              // restore base pointer for this head
	ADDQ BX, SI               // SI += dim*4 (next head)
	JMP ropeHeadLoop

ropeDone:
	RET


