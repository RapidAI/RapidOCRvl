// dotQ8VNNICore(a *int8, xq *uint8, n int) int32
// Computes sum(a[i]*xq[i]) using VPDPBUSD.
// VPDPBUSD: dst = src1(int8, from mem) * src2(uint8, from reg) + dst, per-lane int32 accumulate.
// Each VPDPBUSD on YMM processes 32 int8 * 32 uint8 -> 4 int32 partial sums.
TEXT ·dotQ8VNNICore(SB), NOSPLIT, $0-32
	MOVQ a+0(FP), SI
	MOVQ xq+8(FP), DI
	MOVQ n+16(FP), CX

	VPXORD Y0, Y0, Y0
	VPXORD Y1, Y1, Y1
	VPXORD Y2, Y2, Y2
	VPXORD Y3, Y3, Y3
	VPXORD Y4, Y4, Y4
	VPXORD Y5, Y5, Y5
	VPXORD Y6, Y6, Y6
	VPXORD Y7, Y7, Y7

	CMPQ CX, $32
	JB vnniCoreTail

vnniCoreLoop:
	CMPQ CX, $256
	JB vnniCoreLoop128
	// Process 256 elements: 8 x 32 = 256, 8 independent accumulators
	VMOVDQU (DI), Y8
	VPDPBUSD (SI), Y8, Y0
	VMOVDQU 32(DI), Y9
	VPDPBUSD 32(SI), Y9, Y1
	VMOVDQU 64(DI), Y10
	VPDPBUSD 64(SI), Y10, Y2
	VMOVDQU 96(DI), Y11
	VPDPBUSD 96(SI), Y11, Y3
	VMOVDQU 128(DI), Y12
	VPDPBUSD 128(SI), Y12, Y4
	VMOVDQU 160(DI), Y13
	VPDPBUSD 160(SI), Y13, Y5
	VMOVDQU 192(DI), Y14
	VPDPBUSD 192(SI), Y14, Y6
	VMOVDQU 224(DI), Y15
	VPDPBUSD 224(SI), Y15, Y7
	ADDQ $256, SI
	ADDQ $256, DI
	SUBQ $256, CX
	JMP vnniCoreLoop

vnniCoreLoop128:
	CMPQ CX, $128
	JB vnniCoreLoop64
	VMOVDQU (DI), Y8
	VPDPBUSD (SI), Y8, Y0
	VMOVDQU 32(DI), Y9
	VPDPBUSD 32(SI), Y9, Y1
	VMOVDQU 64(DI), Y10
	VPDPBUSD 64(SI), Y10, Y2
	VMOVDQU 96(DI), Y11
	VPDPBUSD 96(SI), Y11, Y3
	ADDQ $128, SI
	ADDQ $128, DI
	SUBQ $128, CX
	JMP vnniCoreLoop

vnniCoreLoop64:
	CMPQ CX, $64
	JB vnniCoreLoop32
	VMOVDQU (DI), Y8
	VPDPBUSD (SI), Y8, Y0
	VMOVDQU 32(DI), Y9
	VPDPBUSD 32(SI), Y9, Y1
	ADDQ $64, SI
	ADDQ $64, DI
	SUBQ $64, CX
	JMP vnniCoreLoop

vnniCoreLoop32:
	CMPQ CX, $32
	JB vnniCoreReduce
	VMOVDQU (DI), Y8
	VPDPBUSD (SI), Y8, Y0
	ADDQ $32, SI
	ADDQ $32, DI
	SUBQ $32, CX
	JMP vnniCoreLoop32

vnniCoreReduce:
	VPADDD Y1, Y0, Y0
	VPADDD Y3, Y2, Y2
	VPADDD Y5, Y4, Y4
	VPADDD Y7, Y6, Y6
	VPADDD Y2, Y0, Y0
	VPADDD Y6, Y4, Y4
	VPADDD Y4, Y0, Y0
	VEXTRACTI128 $1, Y0, X2
	VPADDD X2, X0, X0
	VPSHUFD $0x4E, X0, X1
	VPADDD X1, X0, X0
	VPSHUFD $0xB1, X0, X1
	VPADDD X1, X0, X0
	VMOVD X0, AX
	VZEROUPPER
	MOVL AX, ret+24(FP)
	RET

vnniCoreTail:
	XORL AX, AX
vnniCoreTailLoop:
	CMPQ CX, $0
	JE vnniCoreTailDone
	MOVBQSX (SI), R8
	MOVBQZX (DI), R9
	IMULL R8, R9
	ADDL R9, AX
	INCQ SI
	INCQ DI
	DECQ CX
	JMP vnniCoreTailLoop
vnniCoreTailDone:
	MOVL AX, ret+24(FP)
	RET

TEXT ·rowSumQ8Asm(SB), NOSPLIT, $0-24
	MOVQ a+0(FP), SI
	MOVQ n+8(FP), CX
	XORL AX, AX
rowSumLoop:
	CMPQ CX, $0
	JE rowSumDone
	MOVBQSX (SI), R8
	ADDL R8, AX
	INCQ SI
	DECQ CX
	JMP rowSumLoop
rowSumDone:
	MOVL AX, ret+16(FP)
	RET

