// dotQ8VNNICore(a *int8, xq *uint8, n int) int32
// Computes sum(a[i]*xq[i]) using VPDPBUSD.
// VPDPBUSD: dst = src1(int8, from mem) * src2(uint8, from reg) + dst, per-lane int32 accumulate.
// Each VPDPBUSD on YMM processes 32 int8 * 32 uint8 -> 4 int32 partial sums.
TEXT ·dotQ8VNNICore(SB), NOSPLIT, $0-32
	MOVQ a+0(FP), SI
	MOVQ xq+8(FP), DI
	MOVQ n+16(FP), CX

	VPXORD Y0, Y0, Y0
	VPXORD Y1, Y1, Y1
	VPXORD Y2, Y2, Y2
	VPXORD Y3, Y3, Y3
	VPXORD Y4, Y4, Y4
	VPXORD Y5, Y5, Y5
	VPXORD Y6, Y6, Y6
	VPXORD Y7, Y7, Y7

	CMPQ CX, $32
	JB vnniCoreTail

vnniCoreLoop:
	CMPQ CX, $256
	JB vnniCoreLoop128
	// Process 256 elements: 8 x 32 = 256, 8 independent accumulators
	VMOVDQU (DI), Y8
	VPDPBUSD (SI), Y8, Y0
	VMOVDQU 32(DI), Y9
	VPDPBUSD 32(SI), Y9, Y1
	VMOVDQU 64(DI), Y10
	VPDPBUSD 64(SI), Y10, Y2
	VMOVDQU 96(DI), Y11
	VPDPBUSD 96(SI), Y11, Y3
	VMOVDQU 128(DI), Y12
	VPDPBUSD 128(SI), Y12, Y4
	VMOVDQU 160(DI), Y13
	VPDPBUSD 160(SI), Y13, Y5
	VMOVDQU 192(DI), Y14
	VPDPBUSD 192(SI), Y14, Y6
	VMOVDQU 224(DI), Y15
	VPDPBUSD 224(SI), Y15, Y7
	ADDQ $256, SI
	ADDQ $256, DI
	SUBQ $256, CX
	JMP vnniCoreLoop

vnniCoreLoop128:
	CMPQ CX, $128
	JB vnniCoreLoop64
	VMOVDQU (DI), Y8
	VPDPBUSD (SI), Y8, Y0
	VMOVDQU 32(DI), Y9
	VPDPBUSD 32(SI), Y9, Y1
	VMOVDQU 64(DI), Y10
	VPDPBUSD 64(SI), Y10, Y2
	VMOVDQU 96(DI), Y11
	VPDPBUSD 96(SI), Y11, Y3
	ADDQ $128, SI
	ADDQ $128, DI
	SUBQ $128, CX
	JMP vnniCoreLoop

vnniCoreLoop64:
	CMPQ CX, $64
	JB vnniCoreLoop32
	VMOVDQU (DI), Y8
	VPDPBUSD (SI), Y8, Y0
	VMOVDQU 32(DI), Y9
	VPDPBUSD 32(SI), Y9, Y1
	ADDQ $64, SI
	ADDQ $64, DI
	SUBQ $64, CX
	JMP vnniCoreLoop

vnniCoreLoop32:
	CMPQ CX, $32
	JB vnniCoreReduce
	VMOVDQU (DI), Y8
	VPDPBUSD (SI), Y8, Y0
	ADDQ $32, SI
	ADDQ $32, DI
	SUBQ $32, CX
	JMP vnniCoreLoop32

vnniCoreReduce:
	VPADDD Y1, Y0, Y0
	VPADDD Y3, Y2, Y2
	VPADDD Y5, Y4, Y4
	VPADDD Y7, Y6, Y6
	VPADDD Y2, Y0, Y0
	VPADDD Y6, Y4, Y4
	VPADDD Y4, Y0, Y0
	VEXTRACTI128 $1, Y0, X2
	VPADDD X2, X0, X0
	VPSHUFD $0x4E, X0, X1
	VPADDD X1, X0, X0
	VPSHUFD $0xB1, X0, X1
	VPADDD X1, X0, X0
	VMOVD X0, AX
	VZEROUPPER
	MOVL AX, ret+24(FP)
	RET

vnniCoreTail:
	XORL AX, AX
vnniCoreTailLoop:
	CMPQ CX, $0
	JE vnniCoreTailDone
	MOVBQSX (SI), R8
	MOVBQZX (DI), R9
	IMULL R8, R9
	ADDL R9, AX
	INCQ SI
	INCQ DI
	DECQ CX
	JMP vnniCoreTailLoop
vnniCoreTailDone:
	MOVL AX, ret+24(FP)
	RET

TEXT ·rowSumQ8Asm(SB), NOSPLIT, $0-24
	MOVQ a+0(FP), SI
	MOVQ n+8(FP), CX
	XORL AX, AX
	VPXOR X0, X0, X0
	VPXOR X1, X1, X1

	CMPQ CX, $32
	JB rowSumTail

rowSumLoop32:
	CMPQ CX, $32
	JB rowSumReduce
	// Load 8 bytes at a time, sign-extend to 8 int16, accumulate
	MOVQ (SI), R8
	MOVQ R8, X2
	VPMOVSXBW X2, X3
	VPADDW X3, X0, X0
	MOVQ 8(SI), R8
	MOVQ R8, X2
	VPMOVSXBW X2, X3
	VPADDW X3, X1, X1
	MOVQ 16(SI), R8
	MOVQ R8, X2
	VPMOVSXBW X2, X3
	VPADDW X3, X0, X0
	MOVQ 24(SI), R8
	MOVQ R8, X2
	VPMOVSXBW X2, X3
	VPADDW X3, X1, X1
	ADDQ $32, SI
	SUBQ $32, CX
	JMP rowSumLoop32

rowSumReduce:
	VPADDW X1, X0, X0
	VPSHUFD $0x4E, X0, X1
	VPADDW X1, X0, X0
	VPSHUFD $0xB1, X0, X1
	VPADDW X1, X0, X0
	VPMOVSXWD X0, X0
	VMOVD X0, AX
	MOVL AX, ret+16(FP)
	VZEROUPPER
	RET

rowSumTail:
	XORL AX, AX
rowSumTailLoop:
	CMPQ CX, $0
	JE rowSumTailDone
	MOVBQSX (SI), R8
	ADDL R8, AX
	INCQ SI
	DECQ CX
	JMP rowSumTailLoop
rowSumTailDone:
	MOVL AX, ret+16(FP)
	RET

// dotQ8PairVNNICore(a *int8, b *int8, xq *uint8, n int) (int32, int32)
// ZMM (512-bit) version: each VPDPBUSD processes 64 int8 values.
// 4 accumulator pairs (Z0-Z3 for a, Z4-Z7 for b), 256 elements per main loop.
TEXT ·dotQ8PairVNNICore(SB), NOSPLIT, $0-40
	MOVQ a+0(FP), SI
	MOVQ b+8(FP), BX
	MOVQ xq+16(FP), DI
	MOVQ n+24(FP), CX
	VPXORD Z0, Z0, Z0
	VPXORD Z1, Z1, Z1
	VPXORD Z2, Z2, Z2
	VPXORD Z3, Z3, Z3
	VPXORD Z4, Z4, Z4
	VPXORD Z5, Z5, Z5
	VPXORD Z6, Z6, Z6
	VPXORD Z7, Z7, Z7
	CMPQ CX, $64
	JB pairVnniTail
pairVnniLoop:
	CMPQ CX, $256
	JB pairVnniLoop128
	// Process 256 elements: 4 x 64 = 256
	VMOVDQU64 (DI), Z8
	VPDPBUSD (SI), Z8, Z0
	VPDPBUSD (BX), Z8, Z4
	VMOVDQU64 64(DI), Z9
	VPDPBUSD 64(SI), Z9, Z1
	VPDPBUSD 64(BX), Z9, Z5
	VMOVDQU64 128(DI), Z10
	VPDPBUSD 128(SI), Z10, Z2
	VPDPBUSD 128(BX), Z10, Z6
	VMOVDQU64 192(DI), Z11
	VPDPBUSD 192(SI), Z11, Z3
	VPDPBUSD 192(BX), Z11, Z7
	ADDQ $256, SI
	ADDQ $256, BX
	ADDQ $256, DI
	SUBQ $256, CX
	JMP pairVnniLoop
pairVnniLoop128:
	CMPQ CX, $128
	JB pairVnniLoop64
	VMOVDQU64 (DI), Z8
	VPDPBUSD (SI), Z8, Z0
	VPDPBUSD (BX), Z8, Z4
	VMOVDQU64 64(DI), Z9
	VPDPBUSD 64(SI), Z9, Z1
	VPDPBUSD 64(BX), Z9, Z5
	ADDQ $128, SI
	ADDQ $128, BX
	ADDQ $128, DI
	SUBQ $128, CX
	JMP pairVnniLoop
pairVnniLoop64:
	CMPQ CX, $64
	JB pairVnniReduce
	VMOVDQU64 (DI), Z8
	VPDPBUSD (SI), Z8, Z0
	VPDPBUSD (BX), Z8, Z4
	ADDQ $64, SI
	ADDQ $64, BX
	ADDQ $64, DI
	SUBQ $64, CX
	JMP pairVnniLoop
pairVnniReduce:
	// Reduce A: Z0-Z3
	VPADDD Z1, Z0, Z0
	VPADDD Z3, Z2, Z2
	VPADDD Z2, Z0, Z0
	VEXTRACTI64X4 $1, Z0, Y1
	VPADDD Y1, Y0, Y0
	VEXTRACTI128 $1, Y0, X2
	VPADDD X2, X0, X0
	VPSHUFD $0x4E, X0, X1
	VPADDD X1, X0, X0
	VPSHUFD $0xB1, X0, X1
	VPADDD X1, X0, X0
	VMOVD X0, AX
	// Reduce B: Z4-Z7
	VPADDD Z5, Z4, Z4
	VPADDD Z7, Z6, Z6
	VPADDD Z6, Z4, Z4
	VEXTRACTI64X4 $1, Z4, Y5
	VPADDD Y5, Y4, Y4
	VEXTRACTI128 $1, Y4, X6
	VPADDD X6, X4, X4
	VPSHUFD $0x4E, X4, X5
	VPADDD X5, X4, X4
	VPSHUFD $0xB1, X4, X5
	VPADDD X5, X4, X4
	VMOVD X4, R8
	VZEROUPPER
	MOVL AX, ret0+32(FP)
	MOVL R8, ret1+36(FP)
	RET
pairVnniTail:
	XORL AX, AX
	XORL R8, R8
pairVnniTailLoop:
	CMPQ CX, $0
	JE pairVnniTailDone
	MOVBQSX (SI), R9
	MOVBQZX (DI), R10
	IMULL R9, R10
	ADDL R10, AX
	MOVBQSX (BX), R11
	IMULL R11, R10
	ADDL R10, R8
	INCQ SI
	INCQ BX
	INCQ DI
	DECQ CX
	JMP pairVnniTailLoop
pairVnniTailDone:
	MOVL AX, ret0+32(FP)
	MOVL R8, ret1+36(FP)
	RET

// dotQ8TripletVNNICore(a *int8, b *int8, c *int8, xq *uint8, n int) (int32, int32, int32)
// ZMM (512-bit) version: each VPDPBUSD processes 64 int8 values.
// 6 accumulators (Z0-Z1 for a, Z2-Z3 for b, Z4-Z5 for c), 128 elements per loop.
TEXT ·dotQ8TripletVNNICore(SB), NOSPLIT, $0-56
	MOVQ a+0(FP), SI
	MOVQ b+8(FP), BX
	MOVQ c+16(FP), R12
	MOVQ xq+24(FP), DI
	MOVQ n+32(FP), CX
	VPXORD Z0, Z0, Z0
	VPXORD Z1, Z1, Z1
	VPXORD Z2, Z2, Z2
	VPXORD Z3, Z3, Z3
	VPXORD Z4, Z4, Z4
	VPXORD Z5, Z5, Z5
	CMPQ CX, $64
	JB tripVnniTail
tripVnniLoop:
	CMPQ CX, $128
	JB tripVnniLoop64
	// Process 128 elements: 2 x 64 = 128
	VMOVDQU64 (DI), Z8
	VPDPBUSD (SI), Z8, Z0
	VPDPBUSD (BX), Z8, Z2
	VPDPBUSD (R12), Z8, Z4
	VMOVDQU64 64(DI), Z9
	VPDPBUSD 64(SI), Z9, Z1
	VPDPBUSD 64(BX), Z9, Z3
	VPDPBUSD 64(R12), Z9, Z5
	ADDQ $128, SI
	ADDQ $128, BX
	ADDQ $128, R12
	ADDQ $128, DI
	SUBQ $128, CX
	JMP tripVnniLoop
tripVnniLoop64:
	CMPQ CX, $64
	JB tripVnniReduce
	VMOVDQU64 (DI), Z8
	VPDPBUSD (SI), Z8, Z0
	VPDPBUSD (BX), Z8, Z2
	VPDPBUSD (R12), Z8, Z4
	ADDQ $64, SI
	ADDQ $64, BX
	ADDQ $64, R12
	ADDQ $64, DI
	SUBQ $64, CX
	JMP tripVnniLoop
tripVnniReduce:
	// Reduce A: Z0+Z1
	VPADDD Z1, Z0, Z0
	VEXTRACTI64X4 $1, Z0, Y1
	VPADDD Y1, Y0, Y0
	VEXTRACTI128 $1, Y0, X2
	VPADDD X2, X0, X0
	VPSHUFD $0x4E, X0, X1
	VPADDD X1, X0, X0
	VPSHUFD $0xB1, X0, X1
	VPADDD X1, X0, X0
	VMOVD X0, AX
	// Reduce B: Z2+Z3
	VPADDD Z3, Z2, Z2
	VEXTRACTI64X4 $1, Z2, Y3
	VPADDD Y3, Y2, Y2
	VEXTRACTI128 $1, Y2, X10
	VPADDD X10, X2, X2
	VPSHUFD $0x4E, X2, X3
	VPADDD X3, X2, X2
	VPSHUFD $0xB1, X2, X3
	VPADDD X3, X2, X2
	VMOVD X2, R8
	// Reduce C: Z4+Z5
	VPADDD Z5, Z4, Z4
	VEXTRACTI64X4 $1, Z4, Y5
	VPADDD Y5, Y4, Y4
	VEXTRACTI128 $1, Y4, X12
	VPADDD X12, X4, X4
	VPSHUFD $0x4E, X4, X5
	VPADDD X5, X4, X4
	VPSHUFD $0xB1, X4, X5
	VPADDD X5, X4, X4
	VMOVD X4, R9
	VZEROUPPER
	MOVL AX, ret0+40(FP)
	MOVL R8, ret1+44(FP)
	MOVL R9, ret2+48(FP)
	RET
tripVnniTail:
	XORL AX, AX
	XORL R8, R8
	XORL R9, R9
tripVnniTailLoop:
	CMPQ CX, $0
	JE tripVnniTailDone
	MOVBQSX (SI), R10
	MOVBQZX (DI), R11
	IMULL R10, R11
	ADDL R11, AX
	MOVBQSX (BX), R13
	IMULL R13, R11
	ADDL R11, R8
	MOVBQSX (R12), R14
	IMULL R14, R11
	ADDL R11, R9
	INCQ SI
	INCQ BX
	INCQ R12
	INCQ DI
	DECQ CX
	JMP tripVnniTailLoop
tripVnniTailDone:
	MOVL AX, ret0+40(FP)
	MOVL R8, ret1+44(FP)
	MOVL R9, ret2+48(FP)
	RET

// quantizeXForVNNIAsm(x_base *float32, xq_base *uint8, n int, inv float32)
// Quantizes float32 x to uint8 with offset 128: q = round(x*inv) + 128, clamped [0,255].
// Processes 16 floats per iteration: two YMM passes, combine with VINSERTI32X4,
// then VPMOVDB to pack 16 int32 -> 16 uint8.
TEXT ·quantizeXForVNNIAsm(SB), NOSPLIT, $0-40
	MOVQ x_base+0(FP), SI
	MOVQ xq_base+8(FP), DI
	MOVQ n+16(FP), CX
	VBROADCASTSS inv+24(FP), Y1
	VBROADCASTSS quantClamp255<>(SB), Y4
	VXORPS Y5, Y5, Y5
	VBROADCASTSS quantOffset128<>(SB), Y3

	CMPQ CX, $16
	JB quantXLoop8

quantXLoop16:
	CMPQ CX, $16
	JB quantXLoop8
	// First 8 floats -> 8 int32 -> 8 uint8 in X0
	VMOVUPS (SI), Y0
	VMULPS Y1, Y0, Y0
	VROUNDPS $8, Y0, Y0
	VADDPS Y3, Y0, Y0
	VMAXPS Y5, Y0, Y0
	VMINPS Y4, Y0, Y0
	VCVTPS2DQ Y0, Y0
	VPMOVDB Y0, X0
	VMOVQ X0, (DI)
	// Second 8 floats -> 8 int32 -> 8 uint8 in X6
	VMOVUPS 32(SI), Y6
	VMULPS Y1, Y6, Y6
	VROUNDPS $8, Y6, Y6
	VADDPS Y3, Y6, Y6
	VMAXPS Y5, Y6, Y6
	VMINPS Y4, Y6, Y6
	VCVTPS2DQ Y6, Y6
	VPMOVDB Y6, X6
	VMOVQ X6, 8(DI)
	ADDQ $64, SI
	ADDQ $16, DI
	SUBQ $16, CX
	JMP quantXLoop16

quantXLoop8:
	CMPQ CX, $8
	JB quantXDone
	VMOVUPS (SI), Y0
	VMULPS Y1, Y0, Y0
	VROUNDPS $8, Y0, Y0
	VADDPS Y3, Y0, Y0
	VMAXPS Y5, Y0, Y0
	VMINPS Y4, Y0, Y0
	VCVTPS2DQ Y0, Y0
	VPMOVDB Y0, X0
	VMOVQ X0, (DI)
	ADDQ $32, SI
	ADDQ $8, DI
	SUBQ $8, CX
	JMP quantXLoop8

quantXDone:
	VZEROUPPER
quantXTail:
	CMPQ CX, $0
	JE quantXRet
	MOVSS (SI), X0
	MULSS X1, X0
	ROUNDSS $8, X0, X0
	ADDSS quantOffset128<>(SB), X0
	MAXSS X5, X0
	MINSS X4, X0
	VCVTSS2SI X0, AX
	MOVB AX, (DI)
	ADDQ $4, SI
	INCQ DI
	DECQ CX
	JMP quantXTail

quantXRet:
	VZEROUPPER
	RET

DATA quantClamp255<>+0(SB)/4, $0x437F0000
GLOBL quantClamp255<>(SB), RODATA, $4

DATA quantOffset128<>+0(SB)/4, $0x43000000
GLOBL quantOffset128<>(SB), RODATA, $4

// dotQ8VNNICoreZMM(a *int8, xq *uint8, n int) int32
// ZMM (512-bit) version of dotQ8VNNICore. Each VPDPBUSD processes 64 int8 values.
TEXT ·dotQ8VNNICoreZMM(SB), NOSPLIT, $0-32
	MOVQ a+0(FP), SI
	MOVQ xq+8(FP), DI
	MOVQ n+16(FP), CX

	VPXORD Z0, Z0, Z0
	VPXORD Z1, Z1, Z1
	VPXORD Z2, Z2, Z2
	VPXORD Z3, Z3, Z3
	VPXORD Z4, Z4, Z4
	VPXORD Z5, Z5, Z5
	VPXORD Z6, Z6, Z6
	VPXORD Z7, Z7, Z7

	CMPQ CX, $64
	JB zmmCoreTail

zmmCoreLoop:
	CMPQ CX, $512
	JB zmmCoreLoop256
	// Process 512 elements: 8 x 64 = 512, 8 independent accumulators
	VMOVDQU64 (DI), Z8
	VPDPBUSD (SI), Z8, Z0
	VMOVDQU64 64(DI), Z9
	VPDPBUSD 64(SI), Z9, Z1
	VMOVDQU64 128(DI), Z10
	VPDPBUSD 128(SI), Z10, Z2
	VMOVDQU64 192(DI), Z11
	VPDPBUSD 192(SI), Z11, Z3
	VMOVDQU64 256(DI), Z12
	VPDPBUSD 256(SI), Z12, Z4
	VMOVDQU64 320(DI), Z13
	VPDPBUSD 320(SI), Z13, Z5
	VMOVDQU64 384(DI), Z14
	VPDPBUSD 384(SI), Z14, Z6
	VMOVDQU64 448(DI), Z15
	VPDPBUSD 448(SI), Z15, Z7
	ADDQ $512, SI
	ADDQ $512, DI
	SUBQ $512, CX
	JMP zmmCoreLoop

zmmCoreLoop256:
	CMPQ CX, $256
	JB zmmCoreLoop128
	VMOVDQU64 (DI), Z8
	VPDPBUSD (SI), Z8, Z0
	VMOVDQU64 64(DI), Z9
	VPDPBUSD 64(SI), Z9, Z1
	VMOVDQU64 128(DI), Z10
	VPDPBUSD 128(SI), Z10, Z2
	VMOVDQU64 192(DI), Z11
	VPDPBUSD 192(SI), Z11, Z3
	ADDQ $256, SI
	ADDQ $256, DI
	SUBQ $256, CX
	JMP zmmCoreLoop

zmmCoreLoop128:
	CMPQ CX, $128
	JB zmmCoreLoop64
	VMOVDQU64 (DI), Z8
	VPDPBUSD (SI), Z8, Z0
	VMOVDQU64 64(DI), Z9
	VPDPBUSD 64(SI), Z9, Z1
	ADDQ $128, SI
	ADDQ $128, DI
	SUBQ $128, CX
	JMP zmmCoreLoop

zmmCoreLoop64:
	CMPQ CX, $64
	JB zmmCoreReduce
	VMOVDQU64 (DI), Z8
	VPDPBUSD (SI), Z8, Z0
	ADDQ $64, SI
	ADDQ $64, DI
	SUBQ $64, CX
	JMP zmmCoreLoop64

zmmCoreReduce:
	// Reduce Z0-Z7 (each has 16 int32 values)
	VPADDD Z1, Z0, Z0
	VPADDD Z3, Z2, Z2
	VPADDD Z5, Z4, Z4
	VPADDD Z7, Z6, Z6
	VPADDD Z2, Z0, Z0
	VPADDD Z6, Z4, Z4
	VPADDD Z4, Z0, Z0
	// Z0 has 16 int32 values. Extract upper 256 bits to Y1, add to lower.
	VEXTRACTI64X4 $1, Z0, Y1
	VPADDD Y1, Y0, Y0
	// Y0 has 8 int32 values. Extract upper 128 bits.
	VEXTRACTI128 $1, Y0, X2
	VPADDD X2, X0, X0
	VPSHUFD $0x4E, X0, X1
	VPADDD X1, X0, X0
	VPSHUFD $0xB1, X0, X1
	VPADDD X1, X0, X0
	VMOVD X0, AX
	VZEROUPPER
	MOVL AX, ret+24(FP)
	RET

zmmCoreTail:
	XORL AX, AX
zmmCoreTailLoop:
	CMPQ CX, $0
	JE zmmCoreTailDone
	MOVBQSX (SI), R8
	MOVBQZX (DI), R9
	IMULL R8, R9
	ADDL R9, AX
	INCQ SI
	INCQ DI
	DECQ CX
	JMP zmmCoreTailLoop
zmmCoreTailDone:
	MOVL AX, ret+24(FP)
	RET
