	VFMADD231PS Y1, Y3, Y2
	ADDQ $32, SI
	ADDQ $32, DI
	ADDQ $32, R8
	SUBQ $8, CX
	JMP f32pfmaLoop8

f32pfmaReduce:
	VADDPS Y4, Y0, Y0
	VADDPS Y6, Y2, Y2
	VEXTRACTF128 $1, Y0, X3
	VADDPS X3, X0, X0
	VHADDPS X0, X0, X0
	VHADDPS X0, X0, X0
	VEXTRACTF128 $1, Y2, X3
	VADDPS X3, X2, X2
	VHADDPS X2, X2, X2
	VHADDPS X2, X2, X2
	VZEROUPPER
