// dotF32FMA: FMA-accelerated dot product. Uses VFMADD231PS to fuse
// the multiply and add into a single uop, roughly doubling throughput
// vs separate VMULPS+VADDPS.
TEXT ·dotF32FMA(SB), NOSPLIT, $32-52
	MOVQ a_base+0(FP), SI
	MOVQ a_len+8(FP), CX
	MOVQ b_base+24(FP), DI

	VXORPS Y0, Y0, Y0
	VXORPS Y2, Y2, Y2
	VXORPS Y4, Y4, Y4
	VXORPS Y6, Y6, Y6
	CMPQ CX, $8
	JB f32fmaTail

f32fmaLoop:
	CMPQ CX, $64
	JB f32fmaLoop32
	VMOVUPS (SI), Y1
	VFMADD231PS (DI), Y1, Y0
	VMOVUPS 32(SI), Y3
	VFMADD231PS 32(DI), Y3, Y2
	VMOVUPS 64(SI), Y1
	VFMADD231PS 64(DI), Y1, Y4
	VMOVUPS 96(SI), Y3
	VFMADD231PS 96(DI), Y3, Y6
	VMOVUPS 128(SI), Y1
	VFMADD231PS 128(DI), Y1, Y0
	VMOVUPS 160(SI), Y3
	VFMADD231PS 160(DI), Y3, Y2
	VMOVUPS 192(SI), Y1
	VFMADD231PS 192(DI), Y1, Y4
	VMOVUPS 224(SI), Y3
	VFMADD231PS 224(DI), Y3, Y6
	ADDQ $256, SI
	ADDQ $256, DI
	SUBQ $64, CX
	JMP f32fmaLoop

f32fmaLoop32:
	CMPQ CX, $32
	JB f32fmaLoop16
	VMOVUPS (SI), Y1
	VFMADD231PS (DI), Y1, Y0
	VMOVUPS 32(SI), Y3
	VFMADD231PS 32(DI), Y3, Y2
	VMOVUPS 64(SI), Y1
	VFMADD231PS 64(DI), Y1, Y4
	VMOVUPS 96(SI), Y3
	VFMADD231PS 96(DI), Y3, Y6
	ADDQ $128, SI
	ADDQ $128, DI
	SUBQ $32, CX
	JMP f32fmaLoop

f32fmaLoop16:
	CMPQ CX, $16
	JB f32fmaLoop8
	VMOVUPS (SI), Y1
	VFMADD231PS (DI), Y1, Y0
	VMOVUPS 32(SI), Y3
	VFMADD231PS 32(DI), Y3, Y2
	ADDQ $64, SI
	ADDQ $64, DI
	SUBQ $16, CX
	JMP f32fmaLoop

f32fmaLoop8:
	CMPQ CX, $8
	JB f32fmaReduce
	VMOVUPS (SI), Y1
	VFMADD231PS (DI), Y1, Y0
	ADDQ $32, SI
	ADDQ $32, DI
	SUBQ $8, CX
	JMP f32fmaLoop8

f32fmaReduce:
	VADDPS Y2, Y0, Y0
	VADDPS Y4, Y0, Y0
	VADDPS Y6, Y0, Y0
	VEXTRACTF128 $1, Y0, X2
	VADDPS X2, X0, X0
	VHADDPS X0, X0, X0
	VHADDPS X0, X0, X0
	VZEROUPPER
	JMP f32fmaTailScalar

f32fmaTail:
	VZEROUPPER
	XORPS X0, X0

f32fmaTailScalar:
	CMPQ CX, $0
	JE f32fmaDone
	MOVSS (SI), X1
	MULSS (DI), X1
	ADDSS X1, X0
	ADDQ $4, SI
	ADDQ $4, DI
	DECQ CX
	JMP f32fmaTailScalar

f32fmaDone:
	MOVSS X0, ret+48(FP)
	RET
