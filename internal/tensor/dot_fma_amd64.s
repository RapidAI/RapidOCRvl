// dotF32FMA: FMA-accelerated dot product. Uses VFMADD231PS to fuse
// the multiply and add into a single uop, roughly doubling throughput
// vs separate VMULPS+VADDPS.
TEXT ·dotF32FMA(SB), NOSPLIT, $32-52
	MOVQ a_base+0(FP), SI
	MOVQ a_len+8(FP), CX
	MOVQ b_base+24(FP), DI
	VXORPS Z0, Z0, Z0
	VXORPS Z1, Z1, Z1
	VXORPS Z2, Z2, Z2
	VXORPS Z3, Z3, Z3
	VXORPS Z4, Z4, Z4
	VXORPS Z5, Z5, Z5
	VXORPS Z6, Z6, Z6
	VXORPS Z7, Z7, Z7
	CMPQ CX, $16
	JB f32fmaTail
f32fmaLoop:
	CMPQ CX, $128
	JB f32fmaLoop64
	VMOVUPS (SI), Z8
	VFMADD231PS (DI), Z8, Z0
	VMOVUPS 64(SI), Z9
	VFMADD231PS 64(DI), Z9, Z1
	VMOVUPS 128(SI), Z10
	VFMADD231PS 128(DI), Z10, Z2
	VMOVUPS 192(SI), Z11
	VFMADD231PS 192(DI), Z11, Z3
	VMOVUPS 256(SI), Z12
	VFMADD231PS 256(DI), Z12, Z4
	VMOVUPS 320(SI), Z13
	VFMADD231PS 320(DI), Z13, Z5
	VMOVUPS 384(SI), Z14
	VFMADD231PS 384(DI), Z14, Z6
	VMOVUPS 448(SI), Z15
	VFMADD231PS 448(DI), Z15, Z7
	ADDQ $512, SI
	ADDQ $512, DI
	SUBQ $128, CX
	JMP f32fmaLoop
f32fmaLoop64:
	CMPQ CX, $64
	JB f32fmaLoop32
	VMOVUPS (SI), Z8
	VFMADD231PS (DI), Z8, Z0
	VMOVUPS 64(SI), Z9
	VFMADD231PS 64(DI), Z9, Z1
	VMOVUPS 128(SI), Z10
	VFMADD231PS 128(DI), Z10, Z2
	VMOVUPS 192(SI), Z11
	VFMADD231PS 192(DI), Z11, Z3
	ADDQ $256, SI
	ADDQ $256, DI
	SUBQ $64, CX
	JMP f32fmaLoop64
f32fmaLoop32:
	CMPQ CX, $32
	JB f32fmaLoop16
	VMOVUPS (SI), Z8
	VFMADD231PS (DI), Z8, Z0
	VMOVUPS 64(SI), Z9
	VFMADD231PS 64(DI), Z9, Z1
	ADDQ $128, SI
	ADDQ $128, DI
	SUBQ $32, CX
	JMP f32fmaLoop32
f32fmaLoop16:
	CMPQ CX, $16
	JB f32fmaReduce
	VMOVUPS (SI), Z8
	VFMADD231PS (DI), Z8, Z0
	ADDQ $64, SI
	ADDQ $64, DI
	SUBQ $16, CX
	JMP f32fmaLoop16
f32fmaReduce:
	VADDPS Z1, Z0, Z0
	VADDPS Z3, Z2, Z2
	VADDPS Z5, Z4, Z4
	VADDPS Z7, Z6, Z6
	VADDPS Z2, Z0, Z0
	VADDPS Z6, Z4, Z4
	VADDPS Z4, Z0, Z0
	VEXTRACTF64X4 $1, Z0, Y1
	VADDPS Y0, Y1, Y1
	VEXTRACTF128 $1, Y1, X2
	VADDPS X2, X1, X1
	VMOVHLPS X1, X1, X2
	VADDPS X2, X1, X1
	VSHUFPS $1, X1, X1, X2
	VADDSS X2, X1, X0
	VZEROUPPER
	MOVSS X0, ret+48(FP)
	RET
f32fmaTail:
	VZEROUPPER
	XORPS X0, X0
f32fmaTailScalar:
	CMPQ CX, $0
	JE f32fmaDone
	MOVSS (SI), X1
	MULSS (DI), X1
	ADDSS X1, X0
	ADDQ $4, SI
	ADDQ $4, DI
	DECQ CX
	JMP f32fmaTailScalar
f32fmaDone:
	MOVSS X0, ret+48(FP)
	RET

