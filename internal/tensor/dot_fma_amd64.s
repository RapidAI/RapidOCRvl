	JB q8fmaLoop64
	// 8 independent accumulators, 16 elements each = 128 per iteration.
	VPMOVSXBD (SI), Z8
	VCVTDQ2PS Z8, Z8
	VFMADD231PS (DI), Z8, Z0
	VPMOVSXBD 16(SI), Z9
	VCVTDQ2PS Z9, Z9
	VFMADD231PS 64(DI), Z9, Z1
	VPMOVSXBD 32(SI), Z10
	VCVTDQ2PS Z10, Z10
	VFMADD231PS 128(DI), Z10, Z2
	VPMOVSXBD 48(SI), Z11
	VCVTDQ2PS Z11, Z11
	VFMADD231PS 192(DI), Z11, Z3
	VPMOVSXBD 64(SI), Z12
	VCVTDQ2PS Z12, Z12
	VFMADD231PS 256(DI), Z12, Z4
	VPMOVSXBD 80(SI), Z13
	VCVTDQ2PS Z13, Z13
	VFMADD231PS 320(DI), Z13, Z5
	VPMOVSXBD 96(SI), Z14
	VCVTDQ2PS Z14, Z14
	VFMADD231PS 384(DI), Z14, Z6
	VPMOVSXBD 112(SI), Z15
