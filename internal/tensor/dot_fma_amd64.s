// dotQ8FMA: FMA-accelerated Q8 dot product. Sign-extends int8 to int32,
// converts to float32, then fuses mul+add via VFMADD231PS.
TEXT ·dotQ8FMA(SB), NOSPLIT, $32-52
	MOVQ a_base+0(FP), SI
	MOVQ a_len+8(FP), CX
	MOVQ b_base+24(FP), DI

	VXORPS Y0, Y0, Y0
	VXORPS Y1, Y1, Y1
	VXORPS Y2, Y2, Y2
	VXORPS Y3, Y3, Y3
	VXORPS Y4, Y4, Y4
	VXORPS Y5, Y5, Y5
	VXORPS Y6, Y6, Y6
	VXORPS Y7, Y7, Y7
	CMPQ CX, $8
	JB q8fmaTail

q8fmaLoop:
	CMPQ CX, $64
	JB q8fmaLoop32
	// 8 independent accumulators for full Zen 4 FMA throughput.
	// Y0-Y7 = accumulators, Y8-Y15 = int8->float32 temp.
	VPMOVSXBD (SI), Y8
	VCVTDQ2PS Y8, Y8
	VFMADD231PS (DI), Y8, Y0
	VPMOVSXBD 8(SI), Y9
	VCVTDQ2PS Y9, Y9
	VFMADD231PS 32(DI), Y9, Y1
	VPMOVSXBD 16(SI), Y10
	VCVTDQ2PS Y10, Y10
	VFMADD231PS 64(DI), Y10, Y2
	VPMOVSXBD 24(SI), Y11
	VCVTDQ2PS Y11, Y11
	VFMADD231PS 96(DI), Y11, Y3
	VPMOVSXBD 32(SI), Y12
	VCVTDQ2PS Y12, Y12
	VFMADD231PS 128(DI), Y12, Y4
	VPMOVSXBD 40(SI), Y13
	VCVTDQ2PS Y13, Y13
	VFMADD231PS 160(DI), Y13, Y5
	VPMOVSXBD 48(SI), Y14
	VCVTDQ2PS Y14, Y14
	VFMADD231PS 192(DI), Y14, Y6
	VPMOVSXBD 56(SI), Y15
	VCVTDQ2PS Y15, Y15
	VFMADD231PS 224(DI), Y15, Y7
	ADDQ $64, SI
	ADDQ $256, DI
	SUBQ $64, CX
	JMP q8fmaLoop

q8fmaLoop32:
	CMPQ CX, $32
	JB q8fmaLoop16
	VPMOVSXBD (SI), Y8
	VCVTDQ2PS Y8, Y8
	VFMADD231PS (DI), Y8, Y0
	VPMOVSXBD 8(SI), Y9
	VCVTDQ2PS Y9, Y9
	VFMADD231PS 32(DI), Y9, Y1
	VPMOVSXBD 16(SI), Y10
	VCVTDQ2PS Y10, Y10
	VFMADD231PS 64(DI), Y10, Y2
	VPMOVSXBD 24(SI), Y11
	VCVTDQ2PS Y11, Y11
	VFMADD231PS 96(DI), Y11, Y3
	ADDQ $32, SI
	ADDQ $128, DI
	SUBQ $32, CX
	JMP q8fmaLoop

q8fmaLoop16:
	CMPQ CX, $16
	JB q8fmaLoop8
	VPMOVSXBD (SI), Y8
	VCVTDQ2PS Y8, Y8
	VFMADD231PS (DI), Y8, Y0
	VPMOVSXBD 8(SI), Y9
	VCVTDQ2PS Y9, Y9
	VFMADD231PS 32(DI), Y9, Y1
	ADDQ $16, SI
	ADDQ $64, DI
	SUBQ $16, CX
	JMP q8fmaLoop

q8fmaLoop8:
	CMPQ CX, $8
	JB q8fmaReduce
	VPMOVSXBD (SI), Y8
	VCVTDQ2PS Y8, Y8
	VFMADD231PS (DI), Y8, Y0
	ADDQ $8, SI
	ADDQ $32, DI
	SUBQ $8, CX
	JMP q8fmaLoop8

q8fmaReduce:
	VADDPS Y1, Y0, Y0
	VADDPS Y3, Y2, Y2
	VADDPS Y5, Y4, Y4
	VADDPS Y7, Y6, Y6
	VADDPS Y2, Y0, Y0
	VADDPS Y6, Y4, Y4
	VADDPS Y4, Y0, Y0
	VEXTRACTF128 $1, Y0, X2
	VADDPS X2, X0, X0
	VHADDPS X0, X0, X0
	VHADDPS X0, X0, X0
	VZEROUPPER
	JMP q8fmaTailScalar

q8fmaTail:
	VZEROUPPER
	XORPS X0, X0

