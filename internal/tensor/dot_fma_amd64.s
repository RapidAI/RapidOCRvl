// dotQ8FMA: FMA-accelerated Q8 dot product. Sign-extends int8 to int32,
// converts to float32, then fuses mul+add via VFMADD231PS.
TEXT ·dotQ8FMA(SB), NOSPLIT, $32-52
	MOVQ a_base+0(FP), SI
	MOVQ a_len+8(FP), CX
	MOVQ b_base+24(FP), DI

	VXORPS Z0, Z0, Z0
	VXORPS Z1, Z1, Z1
	VXORPS Z2, Z2, Z2
	VXORPS Z3, Z3, Z3
	VXORPS Z4, Z4, Z4
	VXORPS Z5, Z5, Z5
	VXORPS Z6, Z6, Z6
	VXORPS Z7, Z7, Z7
	CMPQ CX, $16
	JB q8fmaTail

